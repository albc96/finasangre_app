CREATE OR REPLACE TRIGGER trg_herraje_auto
BEFORE INSERT OR UPDATE ON herrajes_finasangre
FOR EACH ROW
BEGIN
  IF :NEW.fecha_herraje IS NULL THEN
    :NEW.fecha_herraje := SYSDATE;
  END IF;

  :NEW.dia := TO_NUMBER(TO_CHAR(:NEW.fecha_herraje, 'DD'));
  :NEW.mes := TO_NUMBER(TO_CHAR(:NEW.fecha_herraje, 'MM'));
  :NEW.anio := TO_NUMBER(TO_CHAR(:NEW.fecha_herraje, 'YYYY'));
  :NEW.hora := TO_CHAR(:NEW.fecha_herraje, 'HH24:MI');
END;
