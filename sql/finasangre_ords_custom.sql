CREATE OR REPLACE FUNCTION fn_finasangre_json_escape(p_text IN VARCHAR2)
RETURN VARCHAR2
IS
BEGIN
  RETURN REPLACE(
           REPLACE(
             REPLACE(
               REPLACE(NVL(p_text, ''), '\', '\\'),
               '"', '\"'
             ),
             CHR(10), ' '
           ),
           CHR(13), ' '
         );
END;
