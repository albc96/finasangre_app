CREATE OR REPLACE TRIGGER trg_protect_owner_update
BEFORE UPDATE ON usuarios_finasangre
FOR EACH ROW
BEGIN
  IF LOWER(:OLD.email) = 'abrahambc' THEN
    IF LOWER(:NEW.email) <> 'abrahambc'
       OR UPPER(NVL(:NEW.rol, '')) <> 'OWNER'
       OR UPPER(NVL(:NEW.activo, '')) <> 'SI' THEN
      RAISE_APPLICATION_ERROR(-20030, 'No se puede modificar, desactivar ni quitar rol OWNER a abrahambc.');
    END IF;
  END IF;
END;
/

