CREATE OR REPLACE PROCEDURE prc_delete_caballo_finasangre(
  p_id_caballo     IN NUMBER,
  p_delete_history IN VARCHAR2 DEFAULT 'NO'
) AS
  l_count   NUMBER;
  l_deleted NUMBER;
BEGIN
  SELECT COUNT(*)
    INTO l_count
    FROM herrajes_finasangre
   WHERE id_caballo = p_id_caballo;

  IF UPPER(NVL(p_delete_history, 'NO')) = 'SI' THEN
    DELETE FROM herrajes_finasangre WHERE id_caballo = p_id_caballo;
    DELETE FROM caballos_finasangre WHERE id_caballo = p_id_caballo;
    l_deleted := SQL%ROWCOUNT;
  ELSE
    IF l_count > 0 THEN
      RAISE_APPLICATION_ERROR(-20001, 'No se puede eliminar porque tiene herrajes asociados.');
    END IF;

    DELETE FROM caballos_finasangre WHERE id_caballo = p_id_caballo;
    l_deleted := SQL%ROWCOUNT;
  END IF;

  IF l_deleted = 0 THEN
    RAISE_APPLICATION_ERROR(-20002, 'Caballo no encontrado.');
  END IF;
END;
