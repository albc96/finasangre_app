CREATE OR REPLACE PROCEDURE prc_generar_reportes_mensuales_fin AS
  l_inicio DATE := TRUNC(ADD_MONTHS(SYSDATE, -1), 'MM');
  l_fin    DATE := LAST_DAY(ADD_MONTHS(SYSDATE, -1));
  l_mes    NUMBER := EXTRACT(MONTH FROM ADD_MONTHS(SYSDATE, -1));
  l_anio   NUMBER := EXTRACT(YEAR FROM ADD_MONTHS(SYSDATE, -1));

  PROCEDURE ensure_reporte(p_id_herrador NUMBER, p_tipo VARCHAR2) IS
    l_count NUMBER;
    l_total NUMBER;
  BEGIN
    SELECT COUNT(*)
      INTO l_count
      FROM reportes_mensuales_finasangre
     WHERE mes = l_mes
       AND anio = l_anio
       AND tipo_reporte = p_tipo
       AND NVL(id_herrador, -1) = NVL(p_id_herrador, -1);

    IF l_count = 0 THEN
      SELECT COUNT(*)
        INTO l_total
        FROM herrajes_finasangre
       WHERE TRUNC(NVL(
               fecha_herraje,
               CASE
                 WHEN anio > 0 AND mes > 0 AND dia > 0
                 THEN TO_DATE(anio || '-' || mes || '-' || dia, 'YYYY-MM-DD')
               END
             )) BETWEEN l_inicio AND l_fin
         AND (p_id_herrador IS NULL OR id_herrador = p_id_herrador);

      INSERT INTO reportes_mensuales_finasangre (
        id_herrador, periodo_inicio, periodo_fin, mes, anio,
        total_herrajes, pdf_url, enviado_whatsapp, enviado_correo,
        estado, tipo_reporte, fecha_creacion
      ) VALUES (
        p_id_herrador, l_inicio, l_fin, l_mes, l_anio,
        l_total, NULL, 'NO', 'NO',
        'GENERADO', p_tipo, SYSTIMESTAMP
      );
    END IF;
  END;
BEGIN
  FOR h IN (
    SELECT id_herrador
      FROM herradores_finasangre
     WHERE NVL(activo, 'SI') = 'SI'
  ) LOOP
    ensure_reporte(h.id_herrador, 'HERRADOR');
  END LOOP;

  ensure_reporte(NULL, 'GLOBAL');
END;
/

